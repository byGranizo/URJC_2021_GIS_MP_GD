public abstract class Ship {

    private String RegNum;
    // private Propietario;
    // private Propulsor;
    private int CrewNum;

    public String getRegNum() {
        return RegNum;
    }

    public void setRegNum(String regNum) {
        RegNum = regNum;
    }

    public int getCrewNum() {
        return CrewNum;
    }

    public void setCrewNum(int crewNum) {
        CrewNum = crewNum;
    }
}
